package designPatterns.creational.factory;
/**
 Фабричный метод — это порождающий паттерн проектирования,
 который решает проблему создания различных продуктов,
 без указания конкретных классов продуктов.

 Фабричный метод задаёт метод, который следует использовать
 вместо вызова оператора new для создания объектов-продуктов.
 Подклассы могут переопределить этот метод,
 чтобы изменять тип создаваемых продуктов.
 */